package com.bridgelabz;

public class Address {
    String city;
    String state;
    long zip;

    public Address() {
    }

    public String getCity() {
        return city;
    }
    public String getState() {
        return state;
    }
    public long getZip() {
        return zip;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public void setState(String state) {
        this.state = state;
    }
    public void setZip(long zip) {
        this.zip = zip;
    }


}
